    public void displayCBX_customer(){
        Connection con = ConnectSQL.getConnect();
        String sql = "select cName from customer";
        try{
            
            PreparedStatement st = con.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            while(rs.next()){
                cbx_customer.addItem(rs.getString("cName"));
            }
        } catch(Exception e){
            e.printStackTrace();
        } 
    }
    
    public void update_tblproduct_increase(){
        Connection con = ConnectSQL.getConnect();
        String sql = "update pet set pQuantity=? where pID=?";
        try{
            int newqty = qty - Integer.parseInt(txt_quantity.getText());
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, newqty);
            pst.setInt(2, pid);
            int rs = pst.executeUpdate();
            //JOptionPane.showMessageDialog(c, "Update Succecfully!");
            display_pet();
            System.out.print(rs);
        }catch(Exception e){
        }
    }
    
    
    
    
    
    
    

